def validate_examples() -> list[str]:
    """安全验证：检查所有示例是否合规。返回错误列表（空列表表示全部通过）。"""
    import re as _re

    FORBIDDEN_PATTERNS = [
        (r"你这是.{0,6}(症|障碍|问题)", "疑似诊断语言"),
        (r"确诊|诊断.{0,4}为", "诊断结论"),
        (r"一定能好|保证.{0,4}康复|包治", "治疗承诺"),
        (r"去买.{0,6}药|服用.{0,6}药|处方|剂量", "药物建议"),
        (r"不用找医生|别去.?医院", "替代专业帮助"),
        (r"只有我.{0,4}你|你离不开我", "诱导依赖"),
        (r"你应该(做|想|去|改变)", "应当性建议"),
        (r"(自杀|自伤|割腕|上吊|跳楼|结束生命|不想活|寻死)", "高风险内容"),
    ]
    _FORBIDDEN = [_re.compile(p, _re.IGNORECASE) for p, _ in FORBIDDEN_PATTERNS]

    errors: list[str] = []
    for mode, examples in COUNSELING_EXAMPLES.items():
        for i, ex in enumerate(examples):
            answer = ex.get("assistant", "")
            for regex, (label, _) in zip(_FORBIDDEN, FORBIDDEN_PATTERNS):
                if regex.search(answer):
                    errors.append(f"{mode}[{i}] 包含禁止内容[{label}]: {answer[:60]}...")
            if len(answer) < 30:
                errors.append(f"{mode}[{i}] 回答过短 ({len(answer)} 字)")
            if len(answer) > 500:
                errors.append(f"{mode}[{i}] 回答过长 ({len(answer)} 字)")
    return errors
